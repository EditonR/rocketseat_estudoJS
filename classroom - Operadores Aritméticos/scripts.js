//Operadores aritméticos

console.log("SOMA: ", 12 + 8); // Soma é difente de concatenar.
console.log("CONCATENAÇÃO: ", "12" + "8"); // Conctenação - Quando concatemos estamos efetuando uma união de texto.

console.log("SUBTRAÇÃO: ", 12 - 8);

console.log("MULTIPLICAÇÃO: ", 3 * 5.5)

console.log("DIVISÃO: ", 12 / 2);

console.log("RESTO DA DIVISÃO: ", 13 % 2); // Útil em alguns cenários como saber se o número é par ou ímpar.

console.log("EXPONENCIAL/POTENCIA: ", 3 ** 3);

